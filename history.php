            <h1>Important Dates in the History of Epsilon</h1>

<b>1925</b>
    <ul>
        <li>On December 16, Frank Reed Horton establishes the Alpha chapter of Alpha Phi Omega at Lafayette College in Easton, PA.</li>
    </ul>

<b>1927</b>
<ul>
        <li>On October 17, the founders of the Epsilon chapter present a petition for a charter to the Faculty Council of Northeast Missouri State Teacher's College and to the national organization of Alpha Phi Omega. </li>
        <li>On December 13, the 10 Founding Fathers are initiated as brothers of Alpha Phi Omega in the then-new Kirk Auditorium; the members elect Orval B. Craig as the first chapter president and Clark D. Stutler as secretary. </li>
    </ul>
<b>1934-1939</b>
    <ul>
        <li>Epsilon is not active, most likely due to the prevailing economic conditions that limited the number of men able to attend college. </li>
    </ul>
<b>1936</b>
    <ul>
        <li>The Alpha Omega chapter of Alpha Phi Omega is founded on May 17 at the Kirksville College of Osteopathic Medicine. </li>
    </ul>
<b>1939</b>
    <ul>
        <li>Epsilon is reactivated in the spring; the president is George Friedmeyer. </li>
        <li>Epsilon extends honorary membership to Delbert Maddox.</li>
    </ul>
<b>1941</b>
    <ul>
        <li>Epsilon sponsors the first recorded drive at the college for the American Red Cross on Nov. 11; $1,210 is donated. </li>
    </ul>
<b>1942</b>
    <ul>
        <li>Epsilon extends honorary fraternity membership to college President Walter H. Ryle for his continued support of Epsilon’s program of service.</li>
    </ul>
<b>1943</b>
    <ul>
        <li>As its last official act before going inactive, Epsilon donates a war bond to the Student Memorial Fund in support of Allied Forces during World War II.  In one year’s time, more than 90 percent of Epsilon’s active membership was summoned to serve in the war. Nevertheless, faculty advisor Ralph Shane points out in the June 4, 1943 edition of the Teacher’s College Index, “the organization is disbanded only temporarily.”</li>
    </ul>
<b>1944-1946</b>
    <ul>
        <li>Epsilon is again no longer active, apparently because of the lack of men on campus due to serving in World War II. </li>
    </ul>
<b>1946</b>
    <ul>
        <li>Epsilon is re-activated as a chapter in the spring; the president is Arthur S. Huff. </li>
    </ul>
<b>1947</b>
    <ul>
        <li>Arthur Harrington joins the Epsilon chapter as a faculty advisor. </li>
    </ul>
<b>1946-1967</b>
    <ul>
        <li>Epsilon conducts several service projects. In Walter H. Ryle's <i>History of the Teacher's College</i>, there are several projects listed including &quot;helping in campus beautification at Christmas-time by decorating the lighting one of the evergreen trees on the campus; helping the administration each quarter during registration; working with the bloodmobile; helping Blue Key to line up floats for the homecoming parade; directing traffic at football games; ushering at assemblies and intercollegiate athletic games; helping in Red Cross drives; conducting fingerprinting projects on campus; sponsoring a fall camporee for the Boy Scouts of the Thousand Hills District; acting to provide guides on Parent's Day.&quot;<span style>&nbsp; </span>Ryle also adds that Epsilon &quot;stands ready at all times to lend a helping hand.&quot; </li>
    </ul>
<b>1951</b>
    <ul>
        <li>Epsilon hosts its first “Ugly Man On Campus” competition; college President Walter H. Ryle wins the competition.</li>
        <li>The April 12, 1951 edition of the Index notes that Epsilon held its first “Smoker” event in Kirk Auditorium, where chapter members entertained prospective members and discussed the aims of the fraternity while playing cards, listening to records and enjoying refreshments.</li>
    </ul>
<b>1953</b>
    <ul>
        <li>Epsilon Founding Father Alfred T. Hutchinson dies (July 29, 1904 - Aug. 27, 1953) and is interred in Hammond, IN.</li>
    </ul>
<b>1957</b>
    <ul>
        <li>Epsilon hosts the first Ugly Man on Campus competition at the college. </li>
    </ul>
<b>1958</b>
    <ul>
        <li>Epsilon presents a clock to the college that will be used in Kirk Auditorium. </li>
    </ul>
<b>1962</b>
    <ul>
        <li>Epsilon begins a lengthy service as the publisher and editor of the Truman Homecoming programs; this is a job that Epsilon fulfills well into the 1970's. </li>
    </ul>
<b>1963</b>
    <ul>
        <li>Epsilon Founding Father Edwin D. Myers dies (Aug. 5, 1900 - Dec. 11, 1963) and is interred in New Boston, IL.</li>
    </ul>
<b>1967</b>
    <ul>
        <li>The name of the school changes to the Northeast Missouri State College. </li>
        <li>Alpha Phi Omega National Service Fraternity undergoes significant structural changes at its constitutional convention. </li>
        <li>Epsilon Founding Father J. Bryan Caviness dies (Jan. 16, 1908 – Jan. 18, 1967) and is interred at Leadwood Cemetery in Leadwood, MO.</li>
    </ul>
<b>1970</b>
    <ul>
        <li>Epsilon Founding Father James H. Streeter dies (Oct. 4, 1905 – Jan. 27, 1970) and is interred at Plainview Cemetery in Pollock, MO.</li>
    </ul>
<b>1972</b>
    <ul>
        <li>The school's name changes from Northeast Missouri State College to Northeast Missouri State University. </li>
        <li>Epsilon Founding Father Richard S. Griffith dies (Jan. 5, 1904 – May 14, 1972) and is interred at Holy Hope Cemetery in Tucson, AZ.</li>
        <li>Arthur Harrington begins serving as chapter advisor.</li>
    </ul>
<b>1973</b>
    <ul>
        <li>Epsilon installs squirrel boxes on the University Quad, and is therefore responsible for the overpopulation of squirrels on the University campus.<span style>&nbsp; </span>This also leads to a new breed of man-eating squirrels. </li>
    </ul>
<b>1976</b>
    <ul>
        <li>Epsilon visits Camp Thunderbird near Moberly, MO for the first time.<span style>&nbsp;
        </span>The camp is owned by the Boy Scouts of America. </li>
        <li>At the National Convention in Atlanta, GA, Alpha Phi Omega National Service Fraternity votes to extend full active membership to females. </li>
        <li>A Little Sister program is started at Epsilon, with three females in the first pledge class. </li>
    </ul>
<b>1977</b>
    <ul>
        <li>Epsilon opens its membership to females, and Mary Ann Wolf is initiated as the first female member of Epsilon, and serves as president of her pledge class. </li>
        <li>The Epsilon Chapter celebrates its 50th anniversary on Dec. 10, 1977.<span style>&nbsp;
        </span>Alpha Phi Omega National Leadership Development Chair Clark Israel was the keynote speaker for the banquet.<span style>&nbsp; </span>
        Epsilon Founding Father Russell Murfin also spoke during the banquet.
         </li>
        <li>The chapter placed a headstone in the University's Quad to commemorate the members of Epsilon from 1927-1977.<span style>&nbsp; </span> </li>
        <li>Advisor Arthur Harrington celebrates his 30<sup>th</sup> anniversary as an Epsilon advisor. </li>
        <li>Epsilon changes its meeting dates to Tuesday evenings, and also changes its procedures to allow for officer elections each semester, as opposed to yearly.</li>
    </ul>
<b>1979</b>
    <ul>
        <li>The Family Flower concept is begun at Epsilon as the University encourages organizations on campus to "start some new traditions." The family flower heads are Randall Cupp, Yellow Roses; Gary Pagliai, Pink Carnations; Steven Wolf, Red Carnations; Jennifer Gardiner, Red Roses; Dariush Eghbali, White Carnations; Denise Brandt, White Roses. The White Roses died out and became the flower for the advisors. </li>
        <li>Denise Brandt serves as the first female president of Epsilon. </li>
        <li>Epsilon Founding Father Otho L. Barnett dies (July 9, 1905 - Sept. 5, 1979), and is interred at Maple Hills Cemetery in Kirksville, MO.</li>
    </ul>
<b>1980</b>
    <ul>
        <li>Denise Brandt serves as Epsilon's first female president.</li>
        <li>Epsilon wins the ‘Man Mile’ award at the Section 49 Conference.</li>
        <li>Epsilon chapter participates in the annual ‘Keep America Beautiful’ day with service projects throughout Kirksville.</li>
    </ul>

<b>1981</b>
    <ul>
        <li>The first <i>
        Blue and Gold</i> chapter newsletter appears in October and serves to the present day in that function at Epsilon. </li>
        <li>Epsilon hosts the Section 49 Conference.</li>
        <li>Epsilon extends honorary fraternity membership to Carol Race.</li>
    </ul>

<b>1982</b>
    <ul>
        <li>National Executive Director Warren Spoon speaks at Epsilon's annual formal on April 23, located at the Hungry Peddler's Restaurant. The event concluded with a dance at Theta Psi's dance hall. </li>
        <li>Epsilon awards its first chapter Distinguished Service Key to active brother Randall Cupp at its spring formal. </li>
        <li>Epsilon assists in hosting the National Convention in Kansas City, MO. </li>
        <li>Epsilon extends honorary fraternity membership to past University President Walter Ryle for his long support of Epsilon's program of service. </li>
    </ul>
<b>1983</b>
    <ul>
        <li>Epsilon extends honorary fraternity membership to Susan Murfin, wife of Epsilon Founding Father Russell Murfin. </li>
    </ul>
<b>1985</b>
    <ul>
        <li>Northeast Missouri State University changes its mission from a regional school to the only statewide liberal arts public university in Missouri. </li>
        <li>Section 49 Chair Eddie Mackie begins and awards the first Gold Sashes at the 'Golden Eagle Banquet,' Epsilon's formal on April 13.<span style>&nbsp;
        </span>The first recipients were actives Chuck Caverly, Joy Jenkins, Jodi Miezio, Jim Mossop, Mark Rash, Steve Rodgers, Ruth Rogers, and alumnus Randall Cupp. </li>
    </ul>
<b>1986</b>
    <ul>
        <li>Linda &quot;Mom&quot; Caraway joins the Epsilon chapter as an advisor. </li>
        <li>Epsilon hosts the &quot;Spring Into Brotherhood&quot; Section 49 Conference, welcoming Alpha Phi Omega National Executive Director Pat Burke as the keynote speaker. </li>
        <li>Epsilon Founding Father Russell Murfin, 79 years old at the time, is awarded an Epsilon chapter Distinguished Service Key at the Section 49 Conference banquet.</li>
        <li>Epsilon adds the Scouting chair position to the chapter’s executive committee.</li>
        <li>-The Alpha Phi chapter (Washington University) joins Epsilon at Camp Thunderbird.</li>
    </ul>
<b>1987</b>
    <ul>
        <li>Epsilon celebrates its 60<sup>th</sup> anniversary, , and commemorates this event by re-enacting the charter ceremony of 1927.</li>
    </ul>
<b>1989</b>
    <ul>
        <li>Epsilon extends honorary fraternity membership to Steve Brown and John Rowe. </li>
        <li>The fall 1989 pledge class gives the “service log” as its gift to the chapter.  It is a wooden log that bears the fraternity’s Greek letters carved into its side.</li>
        <li>Epsilon Founding Father Lyman F. Williamson dies (April 9, 1907 - July 12, 1989) and is interred at Palm Cemetery in Orlando, FL.</li>
    </ul>
<b>1990</b>
    <ul>
        <li>Epsilon conducts its first Chapter Program Planning Conference (CPPC).<span style>&nbsp;
        </span> </li>
        <li>Epsilon extends honorary fraternity membership to Thomas Smotherman. </li>
        <li>Epsilon is one of several chapters to financially contribute to the construction of the new Alpha Phi Omega National Office building in Independence, MO.  Members of Epsilon attend the Nov. 17 dedication ceremony for the new National Office building.</li>
        <li>Fall 1990 President Brian Moore is called up late in the semester to active armed forces duty in the Middle East for Operation Desert Shield/Desert Storm. The executive committee selects Epsilon advisory chair Linda Caraway to moderate the remainder of the fall chapter meetings until elections.</li>
    </ul>
<b>1991</b>
    <ul>
        <li>To better handle the growing demand for its service, Epsilon amends its bylaws to split its Vice President of Service position into two positions (First Vice President of Large Service Projects, and First Vice President of Regular Service Projects).<span style>&nbsp; </span>This occurred after much chapter debate. </li>
    </ul>
<b>1992</b>
    <ul>
        <li>Dr. Ken Carter joins the Epsilon chapter as an advisor.</li>
        <li>Epsilon celebrates its 65th anniversary, and commemorates the event by planting an oak tree on the Quad at NMSU’s campus.</li>
    </ul>
<b>1994</b>
    <ul>
        <li>To better handle the growing demand for its service, Epsilon amends its bylaws to split its Vice President of Service position into two positions (First Vice President of Large Service Projects, and First Vice President of Regular Service Projects).  This occurred after much chapter debate.</li>
        <li>Epsilon brings 50 new prospective brothers into pledge membership, one of its largest pledge class to date. </li>
        <li>Epsilon hosts the Section 49 Conference. </li>
        <li>Epsilon Founding Father and first chapter president, Orval B. Craig, dies (Aug. 19, 1907 – March 29, 1994) and is interred in Ballwin, MO. </li>
    </ul>
<b>1995</b>
    <ul>
        <li>The last surviving Founding Father of Epsilon, Russell Murfin, dies (Oct. 15, 1906 - Sept. 30, 1995), and is interred at Sabbath Home Cemetery in Adair County. </li>
    </ul>
<b>1996</b>
    <ul>
        <li>Per legislation signed by Missouri Gov. Mel Carnahan, Northeast Missouri State University changes its name to Truman State University.</li>
        <li>Northeast Missouri State University changes its name to Truman State University. </li>
        <li>The paddle wars take on new meaning, as the spring pledge class gives a second paddle to Epsilon. </li>
        <li>Epsilon wins Outstanding Large Organization of the Year Award. </li>
        <li>Alpha Phi Omega adopts a risk management policy at its National Convention in Phoenix, and directs chapters to develop specific chapter policies as well. </li>
    </ul>
<b>1997</b>
    <ul>
        <li>In the spring, the pledging program undergoes significant reformation to better reflect national fraternity standards. </li>
        <li>Epsilon reaches the 150-member mark.  </li>
        <li>Due to its growing size, Epsilon can no longer meet in Science Hall 274, and relocates to Benton Elementary School (at the southeast corner of First and Patterson streets) for its regular chapter meetings. The chapter would regularly meet there until spring 1999. </li>
        <li>Epsilon approves more than 150 bylaw amendments. </li>
        <li>Epsilon runs a record-setting Truman Blood Drive. </li>
        <li>The chapter participates in a limited fashion in its first National Service Week. </li>
        <li>Epsilon wins Truman's Outstanding Organization of the Year award. </li>
    </ul>
<b>1998</b>
    <ul>
        <li>The last surviving Founding Father of Epsilon, Clark D. Stutler, dies (Sept. 19, 1906-Feb. 11, 1998) and is interred at Fairmount Memorial Terrace in Denver, CO.</li>
        <li>Epsilon is awarded the Region VIII Best Service Project Award for Camp Thunderbird. </li>
        <li>Epsilon brothers vote to approve a chapter risk management policy. </li>
        <li>Epsilon begins Campuswide Service Week begins at Truman. </li>
        <li>Epsilon wins Outstanding Large Organization of the Year Award. </li>
        <li>Epsilon reaches the 200-member mark. </li>
        <li>Epsilon hosts the Section 49 Conference on April 3-5. </li>
        <li>Epsilon establishes its chapter Web site for the first time. </li>
        <li>Truman President Jack Magruder personally recruits Epsilon to provide large service projects for the campus. </li>
    </ul>
<b>1999</b>
    <ul>
        <li>In a week devoted to service, Epsilon prompts Truman President Jack Magruder to declare “National Service Week” campus-wide.</li>
        <li>The &quot;Old Couple&quot; tradition begins with Epsilon's spring pledge class.<span style>&nbsp;
        </span>Brothers Kelli Kamp and Dan Horst serve as the first actives to voluntarily re-complete the pledge requirements with the pledges. </li>
        <li>At the Section 49 Conference, hosted by Delta Delta chapter, Epsilon wins the Man-Mile Award, the Spirit Award, the Service Award, and the Roll Call Award. </li>
        <li>Epsilon, along with national service sorority Alpha Sigma Gamma, sponsors 'Eating Disorders Week' for Truman State University students. </li>
        <li>On April 17, Epsilon hosts a Boy Scout Merit Badge Day on campus. </li>
        <li>Epsilon is awarded Outstanding Large Organization of the Year. </li>
        <li>Epsilon sponsors an Ugly Man on Campus competition; Truman President Jack Magruder wins the competition with a hilarious photo of himself. </li>
        <li>Epsilon begins meeting in the newly-renovated Violette Hall 1000. </li>
        <li>Fall 1999 Epsilon President Anne Chopin receives Truman's Outstanding Organization President of the Year Award. </li>
        <li>In a week devoted to service, Epsilon prompted Truman President Jack Magruder to declare National Service Week campus-wide. </li>
        <li>To recognize her support of Epsilon's endeavors, Epsilon grants honorary membership to Kathy Rieck, the University's Director of Admissions. </li>
    </ul>
<b>2000</b>
    <ul>
        <li>At the Region VIII Conference in Norman, OK, Epsilon receives the Man Mile Award. </li>
        <li>The spring pledge class travels to Columbia, MO for the first &quot;Destination Unknown,&quot; a day where pledges travel to a location unknown to them to foster brotherhood and pledge class unity.<span style>&nbsp; </span>This event has since become a staple of Epsilon pledge classes. </li>
        <li>Brother Steve Schnurbusch is crowned as the first &quot;Mr. Epsilon&quot; at the inaugural male beauty pageant at Camp Thunderbird. </li>
        <li>Mr. Tim Barcus joins Epsilon as a chapter advisor.</li>
        <li>In fall 2000, Epsilon endures a risk management crisis involving an off-campus incident that prompts an investigation by the University as well as the national fraternity. Epsilon President Vicki Ahn sets an exemplary leadership example by helping steer Epsilon through the crisis and helping protect Epsilon's charter.</li>
        <li>Gene Owens, longtime friend to Epsilon and Camp Thunderbird ranger, dies on Dec. 18. </li>
        <li>At the National Convention in Philadelphia, Epsilon wins the bid to host the next Region VIII Conference </li>
    </ul>
<b>2001</b>
    <ul>
        <li>Epsilon sponsors and organizes the inaugural “The Big Event” in Kirksville, a day of service that brings campus organizations together to give back to the community. “The Big Event” has since become an annual, featured community service event throughout Kirksville and Truman State University.</li>
        <li>Epsilon welcomes members of the UM-St. Louis petitioning group (later the Sigma Alpha chapter) to Camp Thunderbird. </li>
    </ul>
<b>2002</b>
    <ul>
        <li>Epsilon hosts the Region VIII Conference on Jan. 18-20, with almost 500 brothers attending. </li>
        <li>Epsilon alumnus Jim Roach is elected Section 49 Chair at the Section 49 Conference hosted by Alpha Phi chapter; he is the first Epsilon brother to serve in that position. </li>
        <li>
        Representatives from Epsilon travel to St. Louis for the re-chartering ceremony of the Sigma Alpha chapter (UM-St. Louis) on April 21. </li>
        <li>Recent Epsilon alumna Amy Metzger, who was training to become a pilot, dies in a plane crash near St. Louis on April 23, 2002. Epsilon conducts an Alpha Phi Omega Memorial Ceremony in her memory on Truman's campus in front of Kirk Memorial. Amy's younger sister Diane Metzger, an active in the Delta Delta chapter, delivers the keynote address at Smoker the following semester.  The Fall 2002 pledge class is named after Amy Metzger. </li>
        <li>Pam Ryan joins the Epsilon chapter as an advisor. </li>
        <li>Epsilon attends Camp Jo-Ota near Clarence, MO, for the first time on Nov. 1. </li>
        <li>Epsilon as presented with the "Good Neighbor" award from the American Red Cross for excellence in the blood drives. </li>
        <li>Epsilon wins the &quot;Man Mile Award&quot; as well as the <i>Josiah Frank National Historian's Award</i> at the National Convention in New Orleans. This makes Epsilon a national award-winning chapter for the first time. </li>
    </ul>
<b>2003</b>
    <ul>
        <li>Epsilon hosts its 75<sup>th</sup> anniversary reunion on March 28-29, coinciding with formal.<span style>&nbsp; </span>Alpha Phi Omega National President Bobby Hainline is the keynote speaker, and more than 100 Epsilon alumni return to Kirksville.<span style>&nbsp; </span>Nine of the chapter's Distinguished Service Key recipients are also present for the weekend festivities.<span style>&nbsp;
        </span>This event is highlighted in the fall issue of the fraternity's
        <i>Torch &amp; Trefoil</i> publication. li>
        <li>Epsilon sponsors an Ugly Man on Campus competition. </li>
        <li>Epsilon grants retiring University President Jack Magruder honorary membership for his long support of Alpha Phi Omega on the campus of Truman State University. </li>
        <li>Brother Gabe Kennon is crowned Truman Homecoming King; he was sponsored by Epsilon.<span style>&nbsp;
        </span>The chapter also participates in a weeklong array of Homecoming competitions, including volleyball, Scavenger Hunt, Banner, Street Graffiti, Philanthropy, Spirit, Flag Football, Trivial Pursuit, and Ultimate Frisbee competitions. Epsilon won 2<sup>nd</sup> place Outstanding Organization overall, while winning numerous awards. </li>
        <li>Epsilon grants honorary membership to Allen Caraway, spouse of advisor Linda &quot;Mom&quot; Caraway. </li>
    </ul>
<b>2004</b>
    <ul>
        <li>Epsilon wins the Most Spirited Chapter Award at the Region VIII Conference in Omaha. </li>
        <li>Epsilon spearheads a &quot;Trading Spaces&quot; style service project on Truman's campus, redecorating several residence hall rooms free of charge.<span style>&nbsp;
        </span>This is prominently covered in the <i>Index</i>. </li>
        <li>At the Section 49 Conference hosted by the Beta Psi chapter in Cape Girardeau, MO, Epsilon wins the Man Mile Award and the Road Warriors Award. </li>
        <li>Also at the Section 49 Conference, brothers Nick Harbison and Kristina Sis are Epsilon's first recipients of the new Section 49 Railroaders Award. </li>
        <li>Epsilon wins the Section 49 Fellowship Challenge paddle from the Sigma Alpha chapter. </li>
        <li>Advisor Linda &quot;Mom&quot; Caraway receives the <i>Phyllis C. Tenney Excellence in Advising Award</i> at the National Convention in Denver.<span style>&nbsp; </span>This national fraternity award is given to advisors who provide service to chapters of an exemplary nature for an extended period of time.<span style>&nbsp;
        </span>Many Epsilon alumni and actives submitted nominations on her behalf. </li>
    </ul>
<b>2005</b>
    <ul>
        <li>Epsilon hosts the Section 49 Conference on April 8-9. </li>
        <li>Stephen Hadwiger joins Epsilon as a scouting advisor. </li>
        <li>Revisions were made to the chapter risk management policy and related bylaws. </li>
        <li>Epsilon wins the Man Mile Award at the Region VIII Conference in St. Louis. </li>
    </ul>

<b>2006</b>
    <ul>
        <li>Representatives from Epsilon travel to St. Charles, MO for the re-chartering ceremony of the Alpha Delta Sigma chapter (Lindenwood University). </li>
        <li>Epsilon wins the ‘Man Mile Award’ at the Section 35 Conference in Kansas City, MO.</li>
        <li>Epsilon wins the ‘Man Mile Award’ at the Section 49 Conference in Edwardsville, IL.</li>
        <li>Linda “Mom” Caraway celebrates her 20th anniversary as an Epsilon advisor.</li>
        <li>Epsilon alumnus Dan Clark is elected Section 68 Chair in eastern Tennessee.</li>
        <li>Epsilon receives the Alpha Phi Omega <i>Chapter of Excellence Award</i> at the National Convention in Louisville, KY.</li>
        <li>Susan B. Murfin, honorary member of Epsilon and spouse of deceased Founding Father Russell W. Murfin, dies (July 10, 1909 – July 11, 2006) and is interred alongside her husband at Sabbath Home Cemetery in Adair County, MO.</li>
    </ul>
<b>2007</b>
    <ul>
        <li>Epsilon receives the Alpha Phi Omega <i>Chapter of Excellence</i> Award. </li>
        <li>Epsilon celebrates its 80<sup>th</sup> anniversary with a weekend of activities on March 23-24. </li>
        <li>Alumnus Randall Cupp (Fall 1978) creates the chapter Distinguished Alumni Key at Epsilon's 80<sup>th</sup> anniversary formal. Jim Roach (Spring 1997) is the first recipient. </li>
        <li>Epsilon hosts a successful Ugly Man on Campus competition to raise money for the re-building of the Newman Center, which was destroyed by fire.</li>
    </ul>


<b>2008</b>

    <ul>

        <li>Camp Jo-Ota is moved to Camp T-Bird following a pricing disagreement.</li>
        <li>Epsilon heads south over Spring Break to help build houses for hurricane victims.</li>
        <li>Revisions to Chapter Bylaws are made following a difficult meeting over suspensions in December 2007.</li>
        <li>Epsilon website is redesigned.</li>
        <li>Jill and Matt Crowe become new advisors.</li>
    </ul>


<b>2009</b>

    <ul>

    <li>Epsilon alumnus Philip Slocum (fall 1970) becomes a community advisor for the Chapter.</li>
        <li>Epsilon receives the Alpha Phi Omega Pledge Program of Excellence Award as well as the Chapter of Excellence Award.</li>
        <li>The Boy Scouts of America put Camp Thunderbird near Moberly, MO, up for sale.</li>
        <li>Advisor roles are discussed and new structure is implemented</li>
        <li>Major bylaw revisions to reduce redundancy in the bylaws are made.</li>

    </ul>


<b>2010</b>
    <ul>
        <li>Epsilon alumna Marlene Clark is elected Section 49 Chair at the Section 49 Conference, hosted by Delta Delta chapter. </li>
        <li>Epsilon attends the Silver Meadows Girl Scout camp near Columbia, MO</li>
    </ul>
<b>2011</b>
    <ul>
        <li>The Linda Caraway Excellence In Leadership Award is created and awarded at Epsilon’s spring formal on April 9.  This award is given annually to the active member who, over the course of the academic year, has most consistently displayed the Cardinal principle of Leadership. Brother Liz Bledsoe is Epsilon’s first recipient. </li>
        <li>The Cupp of Brotherhood award is created and awarded at Epsilon’s spring formal on April 9.  This award is given annually to the active member who, over the course of the academic year, has most consistently displayed the Cardinal principle of Friendship. Brother Brendan O’Brien is Epsilon’s first recipient.</li>
        <li>The Murfin Plaids spirit group are created and awarded at Epsilon’s spring formal on April 9, to recognize those brothers who go above and beyond the call of duty in the specific area of scouting & youth services.  Alumni brothers Randall Cupp and Jim Roach are on hand to present the inaugural Murfin Plaids to brothers Patrick O’Donnell, Bridget Rothermich, Justin Swank, Samantha Tucker, & Glee Williams.</li>
        <li>Epsilon welcomes alumna Kara Jo (Levery) Humphrey as a chapter advisor.</li>
        <li>Epsilon welcomes alumna Marlene (Clark) Talbert as a chapter advisor.</li>
    </ul>
<b>2012</b>
    <ul>
