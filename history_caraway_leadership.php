<h1>Linda Caraway Excellence in Leadership Award</h1>
<p>The Linda Caraway Excellence in Leadership Award is given to one active member who, over the course of the academic year, most consistently displays the fraternity's Cardinal principle of Leadership. This member embodies Leadership in a manner that advances the Chapter's ideals while actively displaying creativity, steadfastness, results and goodwill. This award is given by a plurality chapter vote, and is awarded at Epsilon's annual formal.</p>

<p>This is award is named after Linda 'Mom' Caraway, who began her tenure with AFW-Epsilon as an advisor in 1986.  She was awarded a Distinguished Service Key in 1988, and was the namesake for Epsilon's spring 1990 pledge class. Her husband Allen became an honorary member of Epsilon in 2003. In 2004, she received the Phyllis C. Tenney Excellence in Advising Award at the Alpha Phi Omega National Convention in Denver.</p>

